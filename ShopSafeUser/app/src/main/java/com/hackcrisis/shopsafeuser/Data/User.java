package com.hackcrisis.shopsafeuser.Data;

public class User {

    private String userName, email, address, pinCode, healthCondition, phone, age;

    public User(String userName, String email, String address, String pinCode, String healthCondition, String phone, String age) {
        this.userName = userName;
        this.email = email;
        this.address = address;
        this.pinCode = pinCode;
        this.healthCondition = healthCondition;
        this.phone = phone;
        this.age = age;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPinCode() {
        return pinCode;
    }

    public void setPinCode(String pinCode) {
        this.pinCode = pinCode;
    }

    public String getHealthCondition() {
        return healthCondition;
    }

    public void setHealthCondition(String healthCondition) {
        this.healthCondition = healthCondition;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
